# ABOUTME: A simple calculator module with basic arithmetic operations
# ABOUTME: Provides add and subtract functions with proper documentation

def add(a, b):
    """
    Add two numbers together.
    
    Args:
        a: First number
        b: Second number
        
    Returns:
        The sum of a and b
    """
    return a + b


def subtract(a, b):
    """
    Subtract the second number from the first.
    
    Args:
        a: First number (minuend)
        b: Second number (subtrahend)
        
    Returns:
        The difference of a minus b
    """
    return a - b